let a = 3;
let b = 2;

/* Adição */
const add = a + b;

/* Subtração */
const sub = a - b;

/* Multiplicação */
const mult = a * b;

/* Divisão */
const div = a / b;

/* Módulo */
const mod = a % b;

console.log('**************');
console.log("3 + 2 = ", add);
console.log("3 - 2 = ", sub);
console.log("3 * 2 = ", mult);
console.log("3 / 2 = ", div);
console.log("3 % 2 = ", mod);
console.log('**************');